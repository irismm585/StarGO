export function getVenuesByCity(city: string): string[] {
  // Exact match first
  if (VENUES_BY_CITY[city]) {
    return VENUES_BY_CITY[city];
  }
  // Fallback: partial match (e.g. "东京" matches "东京", "大阪" etc.)
  const matched = Object.entries(VENUES_BY_CITY).find(([key]) =>
    city.includes(key) || key.includes(city),
  );
  return matched ? matched[1] : [];
}
